// getSimpleExample - Пример простого буферезированного канала
func getSimpleExample() {
	chInt := make(chan int, 2)

	go func() {
		for i := 0; i < 5; i++ {
			fmt.Printf("Отправляем значение %d в канал.\n", i)
			chInt <- i
			fmt.Println("Значение отправлено.")
		}
	}()

	time.Sleep(1 * time.Second)

	for i := 0; i < 5; i++ {
		fmt.Printf("Чтение значения %d из канала. \n", <-chInt)
	}
}

// Пояснение:
// 	- первые 2 значения уйдут сразу
// 	- на 3 продюсер зависнет, пока main не начнёт читать

// 🔑 Буфер = предел асинхронности, а не “бесконечная свобода”.

