// Задание 1. Зависание
func getTestDeadlock() {
	chInt := make(chan int, 2)

	chInt <- 1
	fmt.Println("Записали значение \"1\" в канал.")

	chInt <- 2
	fmt.Println("Записали значение \"2\" в канал.")

	chInt <- 3 // fatal error: all goroutines are asleep - deadlock!
	fmt.Println("Записали значение \"3\" в канал.")
}

