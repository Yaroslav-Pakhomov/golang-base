// producer - запись канала
func producer(chInt chan int) {
	for i := 0; i < 10; i++ {
		fmt.Println("Пытаюсь записать", i)
		chInt <- i
		fmt.Println("Запись", i)
	}

	close(chInt) // Закрыть канал, чтобы консьюмер мог быть range-ом
}

// consumer - чтение канала
func consumer(chInt chan int) {
	for val := range chInt {
		time.Sleep(1500 * time.Millisecond)
		fmt.Println("Чтение", val)
	}
}

// Вопросы:

// 1. Кто закрывает канал?
// Ответ: Кто записывает в канал, тот и закрывает.
// Формально:
// 	- канал закрывает владелец канала
// 	- обычно это последний продюсер
// 	- закрытие = сигнал «данных больше не будет»
// range на стороне консьюмера — это следствие, а не причина.

// 2. Почему не консьюмер?
// Ответ: Консьюмер не знает, закончились ли данные навсегда. Он не владеет жизненным циклом канала.
// Если консьюмер закроет канал:
// 	- продюсер может попытаться записать
// 	- 💥 panic: send on closed channel

// То есть:
// Читающий не имеет права объявлять “данных больше не будет”

// 3. Что будет, если закрыть раньше?
// Ответ:
// | Ситуация                    | Что будет    |
// | --------------------------- | ------------ |
// | `close(ch)`                 | ✅ безопасно  |
// | чтение из закрытого канала  | ✅ безопасно  |
// | `range` по закрытому каналу | ✅ безопасно  |
// | **запись в закрытый канал** | 💥 **panic** |

// Поэтому:
// 	- если закрыть раньше, но никто больше не пишет → panic не будет
// 	- если кто-то попытается писать после close → 💥 panic

// Идеальная версия ответа (как на собесе 💼)
// 	- Канал закрывает тот, кто в него пишет, потому что только он знает, когда данные закончились.
// 	- Консьюмер не закрывает канал, так как он не владеет каналом и может вызвать panic у продюсера.
// 	- Если канал закрыть раньше времени, то при попытке записи в него возникнет panic.

// -----------------

// Задание 3. Закрытый, но не пустой

func getCloseAndCheck() {
	chInt := make(chan int, 2)
	chInt <- 1
	chInt <- 2

	close(chInt)

	for val := range chInt {
		fmt.Println(val)
	}

	// fmt.Println(<-chInt) // 1
	// fmt.Println(<-chInt) // 2
	// fmt.Println(<-chInt) // 0, продолжает выводит уже пустой канал
}

// Вопросы:
// 1. Будет ли panic?
// Ответ:
// 	- Нет. Канал закрыт, но не стёрт.
// 	- Panic не будет
// 	- И формулировка хорошая: close не очищает канал.
// 	- Уточнение:
// 		* panic бывает только при записи в закрытый канал
// 		* чтение из закрытого канала — всегда безопасно

// 2. Чем отличается <-ch от range ch в этом случае?
// <-ch
// 	- всегда возвращает значение
// 	- если канал закрыт и пуст → возвращает zero value
// 	- не сообщает, что канал закрыт (если не использовать v, ok := <-ch)

// range ch
// 	- читает все значения из буфера
// 	- автоматически останавливается, когда:
// 		* канал закрыт
// 		* и буфер опустел
// 	- никогда не отдаёт zero value

