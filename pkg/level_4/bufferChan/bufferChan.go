// 🔹 База

// 1️⃣ Что такое буферизированный канал?
// Ответ:
// Канал с внутренней очередью фиксированного размера, куда можно отправлять значения без немедленного приёма.

// 2️⃣ В чём ключевое отличие от небуферизированного?
// Ответ:
// 	- небуферизированный → синхронизация “отправил ↔ получил”
// 	- буферизированный → временная асинхронность до заполнения буфера

// 3️⃣ Что произойдёт при записи в полный буфер?
// Ответ:
// Отправляющая горутина заблокируется, пока кто-то не прочитает значение.

// 4️⃣ А при чтении из пустого буфера?
// Ответ:
// Читающая горутина заблокируется, пока не появится значение или канал не закроют.

// -----------------

// 🔹 Поведение и блокировки

// 5️⃣ Может ли буферизированный канал избавить от deadlock?
// Ответ:
// ❌ Нет.
// Он только откладывает блокировку, но не отменяет её.

// 6️⃣ Что означает размер буфера на практике?
// Ответ:
// Это предел асинхронности между продюсером и консьюмером.

// 7️⃣ Правда ли, что ch <- v всегда неблокирующий, если есть буфер?
// Ответ:
// ❌ Нет.
// Он неблокирующий только пока есть свободное место в буфере.

// -----------------

// 🔹 Закрытие канала

// 8️⃣ Что делает close(ch)?
// Ответ:
// 	-  запрещает дальнейшую запись
// 	-  не очищает буфер
// 	-  позволяет дочитать все значения из буфера

// 9️⃣ Что вернёт чтение из закрытого, но не пустого канала?
// Ответ:
// Очередные значения из буфера.

// 🔟 Что вернёт чтение из закрытого и пустого канала?
// Ответ:
// zero value типа
// ok == false при v, ok := <-ch

// 1️⃣1️⃣ Можно ли писать в закрытый канал?
// Ответ:
// ❌ Нет.
// Всегда panic.

