// region Контрольные вопросы

// 1. В каких случаях select блокируется? А в каких — нет?

// Блокируется, если:
// 	- нет default
// 	- ни один case сейчас не готов (нельзя ни прочитать, ни записать)

// Не блокируется, если:
// 	- есть default (он выполнится сразу, если ничего не готово)
// 	- хотя бы один case готов (выберется один из готовых)

// -----------------

// 2. Что произойдёт при чтении из закрытого канала? Что такое ok?

// Чтение из закрытого канала не блокируется:
// 	- возвращает zero value типа канала
// 	- и второй результат ok == false

// Пример:
// v, ok := <-ch
// // ok == false => канал закрыт и данных больше не будет

// -----------------

// 3. Почему закрытый канал «вечно готов» и чем это опасно в select?

// Потому что операция чтения из закрытого канала всегда может выполниться немедленно (возвращает zero value), значит case считается готовым всегда.

// Опасность:
// 	- в select такой case может постоянно выигрывать, и ты получишь бесконечный цикл с чтением нулей, “забивая” остальные ветки.

// Типичный баг:

// 	for {
// 		select {
// 		case v := <-ch: // ch закрыт => всегда готов => v всегда 0
// 			...
// 		}
// 	}

// Правильно:
// 	- читать с ok и выходить
// 	- или отключать case через nil (см. ниже)

// -----------------

// 4. Зачем присваивать каналу nil и как это влияет на select?

// Операции с nil-каналом всегда блокируются:
// 	- чтение из nil блокируется
// 	- запись в nil блокируется

// Поэтому если в select поставить канал в nil, соответствующий case становится навсегда неготовым и фактически “выключается”.

// Классический паттерн “отключить закрытый канал”:
// 	case v, ok := <-ch:
// 		if !ok { ch = nil; break } // выключили ветку

// -----------------

// 5. Чем отличается time.After от time.NewTimer в цикле?

// time.After(d) создаёт новый таймер каждый раз и возвращает канал.

// В цикле это часто плохо:
// 	- создаётся куча таймеров
// 	- если ветка select не выбрана, таймер всё равно “доживёт” и сработает — лишние аллокации/нагрузка

// time.NewTimer(d):
// 	- создаёшь один таймер
// 	- можешь Reset/Stop и переиспользовать
// 	- обычно правильный вариант для циклов/долгоживущих горутин

// -----------------

// 6. Почему default часто приводит к 100% загрузке CPU?

// Потому что select с default никогда не блокируется.
// Если ты в цикле, получается “busy loop”:

// 	for {
// 		select {
// 		case v := <-ch:
// 			...
// 		default:
// 			// сразу снова по кругу
// 		}
// 	}

// Если в default нет Sleep/ожидания/работы, цикл крутится максимально быстро → CPU 100%.

// -----------------

// 7. Чем удобен done-канал и почему его часто close, вместо отправки значения?

// done обычно используют как сигнал отмены/завершения для многих горутин.

// Почему close(done) лучше, чем done <- struct{}{}:
// 	- close будит всех: все, кто ждёт <-done, сразу проснутся
// 	- отправка значения разбудит только одного (если канал не буферизирован) и может заблокироваться
// 	- close не требует “кто-то должен прочитать”

// Типично:
// close(done) // broadcast сигнал всем

// -----------------

// 8. Гарантирует ли select честное распределение между готовыми каналами?

// Нет строгой “честности” (fairness) по очереди он не гарантирует.

// Но:
// 	- Go старается выбирать псевдослучайно среди готовых case, чтобы не было постоянного предпочтения одного и того же.

// На практике это не гарантирует отсутствие голодания в твоей логике, особенно если один case почти всегда готов (например, закрытый канал или постоянно заполненный буфер).

// endregion Контрольные вопросы
