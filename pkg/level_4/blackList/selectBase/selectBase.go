// Задание 3. Остановка по done

// Условие: добавь done <-chan struct{}. При его закрытии агрегатор должен немедленно завершаться, даже если каналы не закрыты.

// Требования:
// 	- Во всех местах ожидания используй select { case <-done: ... }.
// 	- Убедись, что горутина агрегатора не зависает.

// GetDoneChan - создание общего канала и каналов для записи
func GetDoneChan() {
	done := make(chan struct{})

	chA := make(chan int)
	chB := make(chan int)

	// Пишем в a/b, но НЕ закрываем их специально (симуляция условия задачи).
	go fillChan(chA, 1, 5, 70)
	go fillChan(chB, 10, 15, 90)

	out := FanIn(done, chA, chB)

	// Дадим немного поработать и остановим по done
	go func() {
		time.Sleep(5 * time.Second)
		close(done)
	}()

	for v := range out {
		fmt.Println(v)
	}

	fmt.Println("Агрегатор остановлен")
}

// fillChan - записываем значение в канал
func fillChan(ch chan int, startRange, endRange int, silence time.Duration) {
	for i := startRange; i <= endRange; i++ {
		ch <- i
		time.Sleep(silence * time.Millisecond)
	}
}

// FanIn агрегирует значения из нескольких входных каналов в один выходной.
// При закрытии done завершает работу немедленно, не ожидая закрытия входов.
func FanIn(done <-chan struct{}, inputs ...<-chan int) chan int {
	out := make(chan int)

	var wg sync.WaitGroup
	wg.Add(len(inputs))

	// На каждый вход — горутина, читающая и пересылающая в out.
	for _, input := range inputs {
		ch := input
		go getSelectChan(&wg, done, ch, out)
	}

	// Закрываем out, когда все воркеры завершились.
	go closeGenChan(out, &wg, done)

	return out
}

// getSelectChan - получение данных из канала и запись в общий
func getSelectChan(wg *sync.WaitGroup, done <-chan struct{}, ch <-chan int, out chan int) chan int {
	defer wg.Done()

	for {
		select {

		case <-done:
			return out

		case v, ok := <-ch:

			if !ok {
				ch = nil
				continue
			}

			// Отправка тоже может блокировать — поэтому снова select с done.
			select {
			case <-done:
				return out
			case out <- v:
			}
		}
	}
}

// closeGenChan - закрытие общего канала
func closeGenChan(out chan int, wg *sync.WaitGroup, done <-chan struct{}) {
	defer close(out)

	// Ожидание wg.Wait тоже нельзя "select'ить" напрямую,
	// поэтому делаем канал завершения.
	waitCh := make(chan struct{})

	go func() {
		wg.Wait()
		close(waitCh)
	}()

	select {
	case <-done:
		// Немедленный выход: воркеры сами завершатся по done,
		// out закроется здесь.
		return
	case <-waitCh:
		return
	}
}

