// Задание 1. Объединение двух каналов событий (“fan-in”)

// Условие: есть два источника строк: chA, chB. Нужно сделать одну горутину-агрегатор, которая читает из обоих и пишет в out. Когда оба входных канала закрыты — закрыть out.

// Требования:
// 	- Используй select.
// 	- Корректно обработай закрытие каналов через ok и отключение nil.

func readFromTwoChannel() {
	chA := make(chan string)
	chB := make(chan string)

	// запускаем агрегатор ОДИН раз
	out := FanInStrings(chA, chB)
	outWithSilence := FanInStringsSilenceTimeout(chA, chB)
	go func() {
		defer close(chA)
		defer close(chB)

		for i := 1; i <= 5; i++ {
			chA <- fmt.Sprintf("chA %d", i)
			chB <- fmt.Sprintf("chB %d", i)

			time.Sleep(5 * time.Millisecond) // делаем потребителя медленнее
		}

	}()

	fmt.Println("Задача 1")
	for v := range out {
		fmt.Println(v)
	}

	fmt.Println("")
	fmt.Println("Задача 2")
	for v := range outWithSilence {
		fmt.Println(v)
	}
}

// FanInStrings - возвращает <-chan string (наружу писать не надо)
func FanInStrings(chA, chB <-chan string) chan string {
	out := make(chan string)

	go func() {
		defer close(out)

		// локальные переменные-каналы, чтобы можно было заnilить
		a, b := chA, chB

		for a != nil || b != nil {

			select {

			case v, ok := <-a:
				if !ok {
					a = nil
					continue
				}
				out <- v

			case v, ok := <-b:
				if !ok {
					b = nil
					continue
				}
				out <- v

			}
		}
	}()

	return out
}

// Ключевые моменты:
// 	- FanInStrings возвращает <-chan string (наружу писать не надо).
// 	- Закрытый входной канал “отключаем”: a = nil / b = nil, чтобы select больше не выбирал этот кейс.
// 	- out закрывается только когда оба входа стали nil.
// 	- Агрегатор создаётся один раз, а out обязательно читается.

// -----------------

// Задание 2. Агрегатор с тайм-аутом “тишины”

// Условие: читаем из chA и chB. Если 200мс не было ни одного сообщения (тишина), завершаемся: закрываем out и выходим.

// Требования:
// 	- Используй time.NewTimer (не time.After в цикле).
// 	- При получении сообщения таймер «сбрасывается».

// Подсказка: аккуратно с timer.Stop() и чтением из timer.C, если таймер уже успел сработать.

func FanInStringsSilenceTimeout(chA, chB <-chan string) <-chan string {
	out := make(chan string)

	go func() {
		defer close(out)

		const silence = 200 * time.Millisecond

		a, b := chA, chB

		// Таймер
		// таймаут "тишины"
		timer := time.NewTimer(silence)
		defer timer.Stop()

		for {
			// Если оба входа закрыты — можно завершаться сразу
			// (иначе будем ждать "тишину" еще 200ms).
			if a == nil && b == nil {
				return
			}

			select {
			// Прошло 200 мс
			case <-timer.C:
				// 200ms не было сообщений => тишина => выходим
				return

			case v, ok := <-a:
				if !ok {
					a = nil
					continue
				}
				resetTimer(timer, silence)
				out <- v

			case v, ok := <-b:
				if !ok {
					b = nil
					continue
				}
				resetTimer(timer, silence)
				out <- v
			}
		}
	}()

	return out
}

// resetTimer - сброс таймера
func resetTimer(timer *time.Timer, duration time.Duration) {

	// Если таймер уже сработал, Stop() вернет false,
	// и нужно вычитать из timer.C, если там есть значение.
	if !timer.Stop() {
		select {
		case <-timer.C: // вычитываем старое событие
		default:
		}
	}
	timer.Reset(duration)
}

