// Задание 5. Буфер как очередь

// Сделай программу, где:
// 	- канал ch := make(chan string, 5)
// 	- producer отправляет 12 строк вида "msg #i"
// 	- consumer читает и печатает

// Требование: producer должен иногда упираться в заполнение (подсказка: сделай consumer медленнее через time.Sleep).

// Критерии:
// 	- нет дедлоков
// 	- канал корректно закрывается
// 	- чтение через range

// getProducerConsumerStr - получение производителя и потребителя канала со строкой
func getProducerConsumerStr() {
	chStr := make(chan string, 3)

	go producerStr(chStr)
	consumerStr(chStr)
}

// producerStr - производитель канала со строкой
func producerStr(chStr chan<- string) {
	for i := 1; i <= 12; i++ {
		chStr <- fmt.Sprintf("msg #%d", i)
		fmt.Println("Отправлено сообщение в канал, длинна:", len(chStr))
	}

	close(chStr)
}

// consumerStr - потребитель канала со строкой
func consumerStr(chStr <-chan string) {

	for msg := range chStr {
		fmt.Printf("Сообщение канала: %s, длинна: %d. \n", msg, len(chStr))
		time.Sleep(200 * time.Millisecond) // делаем потребителя медленнее
	}

	fmt.Println("Канал закрыт.")
	fmt.Println("")
}

