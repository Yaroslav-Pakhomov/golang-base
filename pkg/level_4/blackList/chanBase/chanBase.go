// Что такое канал в Go

// Канал (chan) — это типизированная “труба” для передачи значений между горутинами.

// Есть два базовых вида:

// 1. Небуферизированный: "make(chan int)"
// 	- Отправка "ch <- x" блокируется, пока кто-то не выполнит чтение "<-ch".
// 	- Чтение "<-ch" блокируется, пока кто-то не отправит значение.

// 2. Буферизированный: "make(chan int, 3)"
// 	- У канала есть внутренний буфер (очередь) на "capacity=3" (ёмкость буфера).
// 	- Отправка не блокируется, пока в буфере есть место.
// 	- Чтение не блокируется, пока в буфере есть элементы.

// -----------------

// “Создай буферизированный канал” — как это делается

// Сигнатура:
// 	ch := make(chan T, N)

// 	- "T" — тип данных (например, int, string, struct{...})
// 	- "N" — размер буфера (ёмкость)

// Пример:
// 	ch := make(chan string, 2)

// -----------------

// Когда происходит блокировка (очень важно)

// Для буферизированного канала:
// 	- "ch <- x" блокируется, когда буфер полон ("len(ch) == cap(ch)").
// 	- "<-ch" блокируется, когда буфер пуст ("len(ch) == 0") и канал не закрыт.

// Можно смотреть:
// 	- "len(ch)" — сколько элементов сейчас в буфере
// 	- "cap(ch)" — ёмкость буфера

// -----------------

// Закрытие канала (close) и поведение чтения

// 	- "close(ch)" означает: новых отправок больше не будет.
// 	- Отправлять в закрытый канал нельзя — будет panic.
// 	- Читать из закрытого канала можно:
// 		* пока есть элементы в буфере — они будут вычитаны
// 		* потом чтение вернёт нулевое значение типа и "ok=false"

// Паттерн:
// 	v, ok := <-ch
// 	if !ok {
// 		// канал закрыт и пуст
// 	}

// или чаще:
// 	for v := range ch {
// 		// читаем, пока канал не закрыт
// 	}

// -----------------

// Разбор частых ошибок

// 1. Дедлок (все горутины ждут)
// 	- например, отправка в полный буфер без читателя, или чтение из пустого без писателя.
// 2. close выполняет не тот, кто “владеет отправкой”
// 	- хорошее правило: закрывает канал тот, кто в него пишет (и точно знает, что больше писать не будет).
// 3. panic: send on closed channel
// 	- кто-то продолжил писать после close.
// 4. Попытка “закрыть канал, чтобы остановить читателя”, но писателей много
// 	- в таких случаях часто нужен context.Context или отдельный “done”-канал, или WaitGroup.

// endregion Теоретическая часть

