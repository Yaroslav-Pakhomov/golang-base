// Задание 3. Producer/consumer с закрытием канала

// Сделай:
// 	- producer отправляет 1..5 и закрывает канал
// 	- consumer читает через range и печатает числа

// getProducerConsumer - вызов последовательно producer и consumer
func getProducerConsumer() {

	chInt := make(chan int)
	go producer(chInt)
	consumer(chInt)
}

// producer - отправляет 1..5 и закрывает канал
func producer(ch chan<- int) {
	for i := 1; i <= 5; i++ {
		ch <- i
	}

	close(ch)
}

// consumer - читает через range и печатает числа
func consumer(ch <-chan int) {
	for v := range ch {
		fmt.Println("получить значение канала:", v)
	}
	fmt.Println("Канал закрыт, выполнен.")
	fmt.Println("")
}

// Внимание:
// 	- producer принимает chan<- int (только отправка)
// 	- consumer принимает <-chan int (только чтение)
// 	- это повышает безопасность архитектуры

