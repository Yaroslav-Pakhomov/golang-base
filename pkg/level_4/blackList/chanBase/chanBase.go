// Задание 7. Worker pool + результаты + сумма квадратов (без утечек)

// Задача:
// 	- jobs := make(chan int, 50)
// 	- results := make(chan int, 50) (буферизированный!)
// 	- 5 воркеров читают jobs, считают square(n) и отправляют в results
// 	- главный поток отправляет 1..100, закрывает jobs
// 	- после окончания воркеров закрой results
// 	- собери сумму всех результатов

// Критерии:
// 	- нет утечек горутин
// 	- results закрывается корректно (обычно через отдельную горутину с WaitGroup)
// 	- сумма печатается

func jobsSqr() {
	jobs := make(chan int, 50)
	results := make(chan int, 50)

	var workerWG sync.WaitGroup
	for i := 1; i <= 5; i++ {
		workerWG.Add(1)
		go workerSqr(i, jobs, results, &workerWG)
	}

	// Пишем jobs и закрываем
	go func() {
		for n := 1; n <= 100; n++ {
			jobs <- n
		}
		close(jobs)
	}()

	// Закрываем results, когда воркеры точно завершили отправку
	go func() {
		workerWG.Wait()
		close(results)
	}()

	sum := 0
	for v := range results {
		sum += v
	}

	fmt.Println("Сумма квадратов 1...100 = ", sum)
	fmt.Println("")
}

func workerSqr(id int, jobs <-chan int, results chan<- int, wg *sync.WaitGroup) {
	defer wg.Done()
	for job := range jobs {
		results <- job * job
	}
}

