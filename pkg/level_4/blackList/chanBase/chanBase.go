// Задание 6. Ограничение параллелизма семафором

// Идея: буферизированный канал можно использовать как семафор.
// Скачай “20 задач” (можно имитировать time.Sleep) и ограничь одновременное выполнение до 3.

// Подсказка-паттерн:
// 	sem := make(chan struct{}, 3)
// 	sem <- struct{}{} // заняли слот
// 	go func() {
// 		defer func() { <-sem }() // освободили слот
// 		// работа
// 	}()

// Критерии:
// 	- одновременно работают максимум 3 горутины (проверь логами)
// 	- программа корректно завершается (WaitGroup)

// semaphore - максимум 3 задачи одновременно (WaitGroup). Проверка “одновременно максимум 3”: в логах start будет идти пачками по 3 (в перемешанном порядке — это нормально).
func semaphore() {
	const total = 20
	sem := make(chan struct{}, 3) // максимум 3 “слота”
	var wg sync.WaitGroup

	for i := 0; i < total; i++ {
		sem <- struct{}{} // заняли слот (может блокироваться, если слотов нет)
		wg.Add(1)

		taskID := i
		go func() {
			defer wg.Done()
			defer func() { <-sem }()

			fmt.Println("start", taskID)
			time.Sleep(300 * time.Millisecond)
			fmt.Println("end", taskID)
		}()
	}

	wg.Wait()
	fmt.Println("Все задачи выполнены.")
	fmt.Println("")
}

