// Задание 1. Минимальный пример буферизированного канала

// Сделай программу, которая:
// 	- создаёт "ch := make(chan int, 3)"
// 	- отправляет в канал 3 числа
// 	- выводит "len(ch)" после каждой отправки
// 	- читает эти 3 числа и выводит "len(ch)" после каждого чтения

func createBufferChan() {
	chInt := make(chan int, 3)

	fmt.Println("ёмкость:", cap(chInt), "длинна:", len(chInt))
	fmt.Println("")

	chInt <- 10
	fmt.Println("Отправка значения \"10\" -> длинна:", len(chInt))

	chInt <- 20
	fmt.Println("Отправка значения \"20\" -> длинна:", len(chInt))

	chInt <- 30
	fmt.Println("Отправка значения \"30\" -> длинна:", len(chInt))
	fmt.Println("")

	// "<-ch" читает значение из канала (получает его)
	fmt.Println("принять:", <-chInt, "len:", len(chInt))
	fmt.Println("принять:", <-chInt, "len:", len(chInt))
	fmt.Println("принять:", <-chInt, "len:", len(chInt))
	fmt.Println("")
}

