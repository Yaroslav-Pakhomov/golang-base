// Задание 4. Мини-очередь задач (worker pool) на buffered channel

// Цель: понять, как буфер помогает складывать задачи, пока воркеры заняты.

// Требования:
// 	- jobs := make(chan int, 10)
// 	- 3 воркера читают jobs и “обрабатывают” (например, печать + небольшая задержка)
// 	- главный поток кладёт 20 задач, потом close(jobs)

// jobs - создаёт Задания для Работы
func jobs() {
	// Работы
	const workersCount = 3
	// Задания
	const jobsCount = 20

	jobs := make(chan int, 10) // <- буфер на 10 задач (очередь)

	var wg sync.WaitGroup
	wg.Add(workersCount)

	// Запускаем воркеров
	for i := 0; i < workersCount; i++ {
		go worker(i, jobs, &wg)
	}

	for j := 0; j < jobsCount; j++ {
		jobs <- j // пока буфер не заполнен, кладём быстро; при заполнении будет блокировка
		fmt.Printf("Главная: задание в очереди %d (Длинна %d/%d)\n", j, len(jobs), cap(jobs))
	}

	close(jobs)
	fmt.Println("Главная: канал заданий закрыт.")

	// Ждём, пока все воркеры дочитают канал и завершатся
	wg.Wait()
	fmt.Println("Главная: все работы выполнены.")
	fmt.Println("")
}

// worker - читает и обрабатывает Задания (jobs)
func worker(id int, jobs <-chan int, wg *sync.WaitGroup) {
	defer wg.Done()

	for job := range jobs {
		fmt.Printf("Работа %d: начато задание %d\n", id, job)

		// имитация работы
		time.Sleep(200 * time.Millisecond)

		fmt.Printf("Работа %d: выполнено задание %d\n", id, job)
	}

	fmt.Printf("Работа %d выполнена полностью\n", id)
}

// Как это работает (по шагам)

// 	- jobs := make(chan int, 10) — буферизированный канал: до 10 задач могут лежать “в очереди”.
// 	- Главная горутина кладёт 20 задач.
// 		* пока len(jobs) < cap(jobs) — запись идёт без ожидания,
// 		* когда буфер заполнится — jobs <- j заблокируется, пока воркер не заберёт задачу.
// 	- Воркеры читают for job := range jobs:
// 		* цикл идёт, пока канал не закрыт и в нём/буфере есть элементы,
// 		* после close(jobs) воркеры дочитают остаток и выйдут.
// 	- WaitGroup нужен, чтобы main не завершился раньше воркеров.

