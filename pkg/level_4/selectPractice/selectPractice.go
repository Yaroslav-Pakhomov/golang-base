// Задание 2. Приоритеты каналов

// Сценарий: есть urgent и normal. Хотим обрабатывать urgent чаще, если он готов.

// Сделай обработчик:
// 	- в первую очередь пытается прочитать urgent (не блокируясь),
// 	- если urgent пуст, тогда блокируется в select на urgent и normal.

// Требование: объясни в комментарии, почему это даёт приоритет.

// В данном решении мы используем механизм select и каналы для того, чтобы гарантировать, что обработка канала urgent будет происходить чаще, если в нем есть данные, а если данных нет — будет происходить блокировка на обоих каналах (urgent и normal). Это позволяет нам обеспечить приоритет для канала urgent, но при этом не блокировать программу, если данных в нем нет.

// chanPriorities - Приоритеты каналов
func chanPriorities() {

	// Создаём два канала: для обычных сообщений и для срочных
	urgent := make(chan string, 1) // Канал urgent
	normal := make(chan string, 1) // Канал normal

	go func() {
		time.Sleep(2 * time.Second)
		urgent <- "Urgent message"
		time.Sleep(2 * time.Second)
		normal <- "Normal message"
	}()

	processed := 0

	// Обработчик сообщений
	for processed < 2 {
		select {
		case msg := <-urgent:
			// Читаем из канала urgent, если он не пуст
			fmt.Println("Канал urgent заполнен:", msg)
			processed++
		default:
			// Если urgent пуст, пытаемся читать из двух каналов с блокировкой
			select {
			case msg := <-urgent:
				// Канал urgent имеет приоритет. Если он заполнен, он будет обработан первым.
				fmt.Println("Канал urgent заполнен:", msg)
				processed++

			case msg := <-normal:
				// Если urgent пуст, читаем из канала normal
				fmt.Println("Канал normal заполнен:", msg)
				processed++
			}
		}
	}
}

// Объяснение кода и приоритета:

// 1. Обработка канала urgent без блокировки:
// В первом select блоке мы пытаемся немедленно прочитать из канала urgent. Этот блок не блокирует выполнение, если канал urgent пуст, т.е. программа сразу же переходит ко второму select. Это позволяет "честно" пытаться читать из канала urgent, если в нем есть данные, без блокировки программы на других операциях.

// 2. Блокировка на обоих каналах (urgent и normal) в случае, если urgent пуст:
// Когда канал urgent пуст, мы используем второй select, в котором каналы urgent и normal будут обрабатываться по очереди. При этом, если urgent снова получит данные, он будет обработан первым, потому что в первом случае мы всегда сначала проверяем urgent.

// 3. Приоритет urgent:
// Приоритет обеспечивается тем, что в случае наличия данных в urgent программа всегда сначала пытается прочитать из этого канала. Если он пуст, то она будет блокироваться на обоих каналах, что позволяет обработать данные из канала normal, но в любом случае при поступлении нового сообщения в канал urgent, программа снова сначала попробует его обработать.

// Таким образом, приоритет в обработке urgent достигается с помощью:
// 	- Проверки канала urgent без блокировки в первой части кода.
// 	- Использования select для блокировки на каналах только когда urgent пуст.
// 	- Переключение на канал normal только при пустом urgent.

