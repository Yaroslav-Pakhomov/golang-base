// 1. Функция FanIn:
// 	- Эта функция принимает канал done для завершения работы и слайс каналов chans (входных каналов).
// 	- Внутри запускается горутина, которая слушает все каналы и передает данные в выходной канал out.
// 	- Когда все входные каналы закрыты или закрыт канал done, горутина завершает свою работу и закрывает выходной канал.

// 2. Как это работает:
// 	- Каждый канал читается в блоке select. Когда данные из канала приходят, они передаются в выходной канал out.
// 	- Если канал закрывается (то есть, если он больше не доступен для чтения), он удаляется из списка отслеживаемых каналов. После этого уменьшится счетчик activeChans, и как только все каналы будут закрыты, горутина завершит свою работу.

// 3. Завершение работы:
// 	- Как только канал done закрыт или все входные каналы закрыты, горутина завершает свою работу, и выходной канал закрывается с помощью defer close(out).

// 4. Тестирование:
// 	- В примере создаются три канала, и через некоторое время они закрываются.
