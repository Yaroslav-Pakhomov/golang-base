// Задание 3. Дедлайн + статистика

// Сделай агрегатор, который:
// 	- читает из двух каналов a и b,
// 	- работает не дольше 2 секунд,
// 	- считает статистику: сколько сообщений пришло из a и сколько из b,
// 	- по завершении печатает статистику.

// Требования:
// 	- дедлайн реализовать через time.NewTimer или context.WithTimeout,
// 	- корректно завершаться при закрытии каналов и/или таймауте.

func readTwoChan() {
	a := make(chan any)
	b := make(chan any)

	go fillChan(a, 7, 120)
	go fillChan(b, 10, 200)

	aggregateWithStats(a, b, 3*time.Second)
}

func fillChan(ch chan<- any, end int, sleep time.Duration) {
	defer close(ch)

	for i := 0; i <= end; i++ {
		ch <- i
		time.Sleep(sleep * time.Millisecond)
	}
}

func aggregateWithStats(a <-chan any, b <-chan any, timeout time.Duration) {
	// даёт дедлайн
	ctx, cancel := context.WithTimeout(context.Background(), timeout)

	defer cancel()

	var counterA, counterB int
	aOpen, bOpen := true, true

	for aOpen || bOpen {

		select {
		case <-ctx.Done():
			fmt.Printf("Выполнено: пауза(%d)", timeout)
			fmt.Printf("Статистика: \"а\"=%d, \"b\"=%d \n", counterA, counterB)
			return

		case _, ok := <-a:
			if !ok {
				aOpen = false
				continue
			}
			counterA++

		case _, ok := <-b:
			if !ok {
				bOpen = false
				continue
			}
			counterB++
		}
	}

	fmt.Println("Выполнено: оба канала закрыты")
	fmt.Printf("Статистика: канал \"а\"=%d, канал \"b\"=%d \n ", counterA, counterB)
}

