// Задание 1. Мультиплексор N каналов (fan-in N)

// Сделай функцию:
// func FanIn[T any](done <-chan struct{}, chans ...<-chan T) <-chan T

// [T any] — это параметризированный тип. Он позволяет писать функции и структуры, которые могут работать с любыми типами данных, вместо того чтобы жестко задавать один конкретный тип.
// 	* T — это параметр типа. Он может быть любым типом, и его значение будет определяться при вызове функции или создании экземпляра структуры.
// 	* any — это синоним для типа interface{}, который может представлять любой тип. Таким образом, T any означает, что T может быть любым типом.

// Здесь:
// 	- [T any] говорит компилятору Go, что функция FanIn является генерик-функцией и использует параметр типа T.
// 	- T может быть любым типом, и мы можем передать в функцию каналы, которые передают значения типа T (например, int, string, float64 и т.д.).
// 	- Ограничения (например, comparable, any) — это набор правил, ограничивающие возможные типы, которые могут быть использованы с генериками. Например, comparable гарантирует, что тип поддерживает операцию сравнения, any — это синоним для типа interface{}, который может представлять любой тип.
// 	- chans ...<-chan T — это слайс каналов, где каждый канал передает значение типа T.
// 	- Возвращаемое значение функции — это канал, который передает значения типа T.

// Поведение:
// 	- читает из всех входных каналов и пишет в out;
// 	- завершает работу, когда:
// 		* закрыт done, или
// 		* закрыты все входные каналы;
// 	- закрывает out при завершении.

// Ограничение: использовать только одну горутину-агрегатор (без горутины на каждый канал).

// Подсказка: для N каналов придётся аккуратно организовать select. (Можно начать с N=3 как фиксированного, а потом усложнить.)

// Критерии приёмки:
// 	- нет зависаний при закрытии каналов;
// 	- нет паник;
// 	- out закрывается ровно один раз;
// 	- не течёт горутина.

// Задача подразумевает создание мультиплексора (fan-in) для нескольких каналов с использованием одной горутины-агрегатора. Мы должны обеспечить корректное чтение данных из всех входных каналов и передать их в выходной канал, завершив работу при закрытии канала done или всех входных каналов.

// Для этого нам нужно будет использовать select, чтобы переключаться между несколькими каналами, а также следить за завершением работы. Один из ключевых моментов — это закрытие выходного канала один раз, когда все каналы либо закрыты, либо канал done закрыт.

func CreateChan() {

	done := make(chan struct{})

	ch1 := make(chan int)
	ch2 := make(chan int)
	ch3 := make(chan int)

	// Запуск мультиплексора
	out := FanIn(done, ch1, ch2, ch3)

	// Публикуем данные
	go func() {
		ch1 <- 1
		ch2 <- 2
		ch3 <- 3
		close(ch1)
		close(ch2)
		close(ch3)
	}()

	// Чтение из выходного канала
	for v := range out {
		fmt.Println(v)
	}

	// Завершаем выполнение
	close(done)
}

func FanIn[T any](done <-chan struct{}, chans ...<-chan T) <-chan T {
	// Канал для передачи результатов
	out := make(chan T)

	var wg sync.WaitGroup
	wg.Add(len(chans))

	for _, ch := range chans {
		ch := ch
		go func() {
			defer wg.Done()
			for {
				select {
				case <-done:
					return
				case v, ok := <-ch:
					if !ok {
						return
					}
					select {
					case <-done:
						return
					case out <- v:
					}
				}
			}
		}()
	}

	go func() {
		wg.Wait()
		close(out)
	}()

	return out
}

