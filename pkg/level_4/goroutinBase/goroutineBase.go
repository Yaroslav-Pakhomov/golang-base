// region Теоретическая часть

// Что такое goroutine

// Goroutine — это “лёгкий поток” в Go. Запускается ключевым словом go перед вызовом функции:
// 	go someFunction()

// После этого:
// 	- someFunction() начинает выполняться параллельно (конкурентно) с остальным кодом.
// 	- Управление в текущей функции не ждёт завершения горутины.

// -----------------

// Главная “ловушка” для новичков

// В Go программа завершается, когда заканчивается функция main().
// Если main() закончилась — все горутины прекращаются, даже если они ещё не успели допечатать числа.

// Отсюда вывод: нужно синхронизировать завершение main() с работой горутин.

// -----------------

// Способы синхронизации (по важности)
// 	- sync.WaitGroup — самый частый и удобный способ “подождать горутины”.
// 	- Каналы (chan) — хорошо, когда надо не просто ждать, а ещё и обмениваться данными/сигналами.
// 	- time.Sleep — не гарантирует корректность (может быть слишком мало/слишком много), поэтому это учебный костыль.

// endregion Теоретическая часть

// region Практическая часть

