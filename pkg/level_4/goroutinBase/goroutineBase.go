	// Ключевое слово go - Запусти эту функцию параллельно, не жди, пока она завершится
	go sayHello()

	// Задание 1
	go sayBye()

	// Задание 2
	go printOne()
	go printTwo()

	// Задание 3
	go circleGoroutine()

