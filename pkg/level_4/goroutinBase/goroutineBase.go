// region Контрольные вопросы-ответы

// 1. Что такое горутина?
// **Ответ:**
// Горутина — это лёгкий поток выполнения в Go, которым управляет runtime Go, а не операционная система.

// ---

// 2. Как запустить функцию в горутине?
// **Ответ:**
// С помощью ключевого слова `go` перед вызовом функции:

// ```go
// go sayHello()
// ```

// ---

// 3. В чём разница между `sayHello()` и `go sayHello()`?
// **Ответ:**
// * `sayHello()` — обычный вызов, `main` ждёт завершения функции
// * `go sayHello()` — функция запускается параллельно, `main` **не ждёт** её завершения

// ---

// 4. Будет ли `main` ждать завершения горутины?
// **Ответ:**
// Нет. `main` продолжает выполняться сразу после запуска горутины.

// ---

// 5. Что произойдёт с горутинами, если `main` завершится?
// **Ответ:**
// Все горутины будут **принудительно завершены**, даже если они ещё не выполнились.

// ---

// 6. Зачем в примере используется `time.Sleep`?
// **Ответ:**
// Чтобы `main` не завершился слишком рано и дал горутине время выполниться.
// Это **учебный приём**, а не правильный способ синхронизации.

// ---

// 7. Почему `time.Sleep` — плохое решение в реальном коде?
// **Ответ:**
// Потому что:
// * нет гарантии, что горутина успеет выполниться
// * задержка выбирается «на глаз»
// * код становится хрупким и непредсказуемым

// ---

// 8. Чем обычно заменяют `time.Sleep` для ожидания горутин?
// **Ответ:**
// * `sync.WaitGroup`
// * каналы (`chan`)

// ---

// 9. Гарантирован ли порядок вывода сообщений?
// **Ответ:**
// Нет. Порядок выполнения горутин **не гарантирован**.

// ---

// 10. Какие варианты вывода возможны?
// **Ответ:**
// Оба варианта корректны:

// ```text
// 	Привет из main
// 	Привет из горутины!
// ```

// или

// ```text
// 	Привет из горутины!
// 	Привет из main
// ```

// ---

// 11. Можно ли запускать тысячи горутин?
// **Ответ:**
// Да. Горутины очень дешёвые по ресурсам, тысячи и даже сотни тысяч — нормально.

// ---

// 12. Являются ли горутины потоками ОС?
// **Ответ:**
// Нет. Это абстракция Go, runtime сам распределяет горутины по потокам ОС.

// ---

// 13. Что означает фраза «параллельно» в контексте горутин?
// **Ответ:**
// Горутины могут:
// * выполняться одновременно (на нескольких ядрах)
// * или переключаться по времени на одном ядре
// Это решает runtime Go.

// ---

// 14. Можно ли запустить анонимную функцию в горутине?
// **Ответ:**
// Да:

// ```go
// 	go func() {
// 		fmt.Println("Привет!")
// 	}()
// ```

// ---

// 15. Главное правило, которое нужно запомнить про горутины?
// **Ответ:**
// > Если `main` завершился — всё закончилось.
// > Горутины сами по себе программу не держат.
	
// endregion Контрольные вопросы-ответы
