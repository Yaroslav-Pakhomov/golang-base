package goroutinBase

import (
	"fmt"
	"sync"
)

// region Теоретическая часть

// Что такое goroutine

// Goroutine — это “лёгкий поток” в Go. Запускается ключевым словом go перед вызовом функции:
// 	go someFunction()

// После этого:
// 	- someFunction() начинает выполняться параллельно (конкурентно) с остальным кодом.
// 	- Управление в текущей функции не ждёт завершения горутины.

// -----------------

// Главная “ловушка” для новичков

// В Go программа завершается, когда заканчивается функция main().
// Если main() закончилась — все горутины прекращаются, даже если они ещё не успели допечатать числа.

// Отсюда вывод: нужно синхронизировать завершение main() с работой горутин.

// -----------------

// Способы синхронизации (по важности)
// 	- sync.WaitGroup — самый частый и удобный способ “подождать горутины”.
// 	- Каналы (chan) — хорошо, когда надо не просто ждать, а ещё и обмениваться данными/сигналами.
// 	- time.Sleep — не гарантирует корректность (может быть слишком мало/слишком много), поэтому это учебный костыль.

// endregion Теоретическая часть

// region Практическая часть

func GetGoroutineBase() {
	// объект ожидания
	var wg sync.WaitGroup

	// есть одна работа, которую нужно дождаться (ожидаем 1 горутину)
	wg.Add(3)

	// запускаем горутину (порядок не гарантирован)
	go printNumbers(&wg)
	go printRange("A", 10, 15, &wg)
	go printRange("B", 100, 105, &wg)

	// блокирует метод GetGoroutineBase() до тех пор, пока счётчик (wg) не станет 0
	wg.Wait()

	// Горутины через канал (chan)
	done := make(chan struct{})
	go printNumbersThroughChan(done)

	<-done
}

// -----------------

// Важные замечания (типичные ошибки):
// 	- Забыли wg.Add(1) до запуска горутины. Иногда приводит к панике или к тому, что Wait() не ждёт.
// 	- Забыли wg.Done(). Тогда Wait() будет ждать вечно.
// 	- Делаете Add внутри горутины. Это часто гонка: Wait() может выполниться раньше, чем Add.
// 	- Думаете, что “go значит новый поток”. Нет, это абстракция, Go сам планирует выполнение.

// -----------------

// Задание 1. Печатник чисел

// Напишите программу, которая запускает горутину, печатающую числа от 1 до 5, и корректно ждёт её завершения.
// Требование: использовать WaitGroup.

// Критерий: программа всегда печатает все 5 чисел и только потом завершается.

// printNumbers - печать от 1 до 5
func printNumbers(wg *sync.WaitGroup) {
	// в конце горутины уменьшит счётчик на 1
	defer wg.Done() // сообщаем WaitGroup, что горутина завершилась

	for i := 1; i <= 5; i++ {
		fmt.Println(i)
	}
	fmt.Println("")
}

// -----------------

// Задание 2. Два диапазона

// Запустите две горутины:
// 	- первая печатает числа 10…15
// 	- вторая печатает числа 100…105

// Добавьте к печати “метку” (A: и B:), чтобы видеть перемешивание.

// Требование: WaitGroup, Add(2).

// printRange - печать чисел в диапазоне от start до end
func printRange(name string, start, end int, wg *sync.WaitGroup) {
	defer wg.Done()

	for i := start; i <= end; i++ {
		fmt.Printf("%s: %d\n", name, i)
	}
	fmt.Println("")
}

// -----------------

// Задание 3. Канал как “сигнал завершения”

// printNumbersThroughChan - горутина, через канал как “сигнал завершения” (альтернатива)
// Если ждать нужно одну горутину, можно использовать канал как сигнал “готово”.
func printNumbersThroughChan(done chan struct{}) {
	for i := 21; i <= 25; i++ {
		fmt.Println(i, "chan")
	}

	// сигнал об окончании
	close(done)
}

