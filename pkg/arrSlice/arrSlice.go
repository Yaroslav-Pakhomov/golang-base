package arrSlice

import "fmt"

// Теоретическая часть

// Массив в Go имеет фиксированную длину и является частью типа:
// var a [3]int // длина 3 — часть типа

// Срез — это “окно” (view) на некоторый массив, длина может меняться:
// var s []int // длина не фиксирована

// Главная мысль: срез — основной инструмент для динамических коллекций в Go.

// ===================================================================================

// Что внутри slice

// Срез концептуально содержит:
// 	- указатель на массив (подлежащие данные),
// 	- len — длину (сколько элементов доступно),
// 	- cap — вместимость (сколько можно “расти” без перераспределения).

// Полезные встроенные функции:
// 	- len(s) — длина
// 	- cap(s) — вместимость

// ===================================================================================

// Создание срезов

// Нулевой срез (nil):
// var s []int // nil, len=0, cap=0

// Литерал:
// s := []int{1, 2, 3}

// Через make:
// s := make([]int, 5)       // len=5, cap=5 (заполнен нулями)
// t := make([]int, 0, 10)   // len=0, cap=10 (удобно для append)

// ===================================================================================

// Добавление элементов: append

// s := []int{1, 2}
// s = append(s, 3)          // [1 2 3]
// s = append(s, 4, 5, 6)    // можно несколько

// Важно: append может создать новый массив, если cap не хватает. Поэтому часто пишут s = append(s, ...).

// ===================================================================================

// Срезание: a[b:c]
// a := []int{10, 20, 30, 40, 50}
// x := a[1:4] // элементы с индекса 1 по 3 включительно => [20 30 40]

// Правила:
// 	- левая граница включительно,
// 	- правая — не включительно.

// ===================================================================================

// Copy и независимое копирование

// Присваивание среза не копирует данные:
// a := []int{1,2,3}
// b := a
// b[0] = 99
// a тоже изменится: [99 2 3]

// Чтобы сделать независимую копию:
// a := []int{1,2,3}
// b := make([]int, len(a))
// copy(b, a)

// ===================================================================================

// Удаление элемента по индексу

// Классический шаблон:
// s = append(s[:i], s[i+1:]...)

// Это меняет порядок элементов (но обычно сохраняет).

func WorkWithSlice() {
	ObserveLenCap()
	fmt.Println("")

	s := []int{1, 2, 3, 4, 5}

	s = InsertAt(s, 2, 99)
	fmt.Println("Преобразованный срез:", s)
	fmt.Println("")

	out := RemoveAll(s, 3)
	fmt.Println("Преобразованный срез:", out)
	fmt.Println("")

	CopySlice()
}

// Задание 1: Наблюдаем len/cap и рост

// 1. Создайте s := make([]int, 0, 2).
// 2. В цикле добавьте 10 чисел (например, 0..9).
// 3. После каждого append печатайте:
// 		- значение s,
// 		- len(s),
// 		- cap(s).

// ObserveLenCap - Наблюдаем len/cap и рост
func ObserveLenCap() {
	s := make([]int, 0, 2)

	fmt.Printf("s=%v len=%d cap=%d\n", s, len(s), cap(s))

	for i := 0; i < 10; i++ {
		s = append(s, i)
		fmt.Printf("s=%v len=%d cap=%d\n", s, len(s), cap(s))
	}
}

// Задание 2: Вставка элемента в середину

// Требования:
// 	- вставляет value по индексу idx,
// 	- сохраняет порядок остальных элементов,
// 	- возвращает новый срез.

// Подсказка-идея:
// 	- расширить s на 1 через append,
// 	- сдвинуть хвост,
// 	- записать элемент.

// InsertAt - Вставка элемента в середину
func InsertAt(s []int, idx int, value int) []int {
	if idx < 0 || idx >= len(s) {
		return s
	}

	// Увеличиваем длину среза на 1
	s = append(s, 0)

	// копируем эл-т с индексом idx, тем самым сдвигая хвост среза
	copy(s[idx+1:], s[idx:])

	s[idx] = value

	return s
}

// Задание 3: Удаление всех вхождений

// Требования:
// 	- удаляет все элементы равные value,
// 	- использует “фильтрацию” (сбор подходящих),
// 	- без лишних аллокаций по возможности (можно переиспользовать исходный массив).

// RemoveAll - Удаление всех вхождений
func RemoveAll(s []int, value int) []int {
	out := make([]int, 0, len(s))

	for _, v := range s {
		if v != value {
			out = append(out, v)
		}
	}

	return out
}

// Задание 4: Копия и влияние изменений

// 	 - Создайте срез a := []int{1,2,3,4}.
// 	 - Сделайте b := a[1:3].
// 	 - Измените b[0] = 999.
// 	 - Выведите a и b.
// 	 - Затем сделайте независимую копию c через copy и проверьте, что изменения c не влияют на a.

func CopySlice() {

	a := []int{1, 2, 3, 4, 5}
	b := a[1:3]

	b[0] = 999

	fmt.Printf("Срез \"a\" s=%v len=%d cap=%d\n", a, len(a), cap(a))
	fmt.Printf("Срез \"b\" s=%v len=%d cap=%d\n", b, len(b), cap(b))

	c := make([]int, len(a))
	copy(c, a)

	fmt.Printf("Срез \"с\" s=%v len=%d cap=%d\n", c, len(c), cap(c))

	c[0] = 150
	c[1] = 155

	fmt.Printf("Срез \"a\" s=%v len=%d cap=%d\n", a, len(a), cap(a))
	fmt.Printf("Срез \"c\" s=%v len=%d cap=%d\n", c, len(c), cap(c))
}
